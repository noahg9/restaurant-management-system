package be.kdg.programming5.programming5.domain;

/**
 * Enum representing different courses in a restaurant menu.
 */
public enum Course {
    /**
     * Starter course.
     */
    Starter,
    /**
     * Main course.
     */
    Main,
    /**
     * Dessert course.
     */
    Dessert,
    /**
     * Beverage course.
     */
    Beverage,
    /**
     * Undefined course.
     */
    Other
}
